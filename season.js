const season = "夏"

switch (season) {
  case "春":
    console.log("花見にいきます");
    break;
  case "夏":
    console.log("プールに行きます");
    break;
  case "秋":
    console.log("栗を拾います");
    break;
  case "冬":
    console.log("餅を食べます");
    break;
  default:
    console.log("勉強をします");

}

